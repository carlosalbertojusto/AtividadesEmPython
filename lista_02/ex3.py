# Faça um Programa que verifique se o numero digitado é "1" ou "2". Conforme o numro
# escrever: 1- Feminino, 2-Masculino ou Sexo Inválido.


sexo = int(input("Digite o sexo: 1. Feminino ou 2. Masculino "))

if sexo == 1: print("Feminino! ")
elif sexo == 2: print("Masculino!")
else : print("Sexo inválido! ")